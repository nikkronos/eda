"""Живой регрессионный прогон LLM-парсера (стоит копейки, ~40 сек).

Использование (нужен ключ):
    ANTHROPIC_API_KEY=... python scripts/llm_smoke.py

Кейсы собраны из реальных сообщений обкатки 05–08.07.2026 — каждый закрывал
живую ошибку (см. ROADMAP «Сделано», калибровки №1–10). При правке SYSTEM_PROMPT
в bot/llm.py гонять ВЕСЬ набор: фикс одного кейса не должен ломать остальные.
Ожидания — в комментарии каждого кейса; вариативность модели допустима, если
инварианты держатся (не обнулять упаковку порцией, не сливать разные продукты,
обрывки → chatter, оценки не выдумывать).
"""
import asyncio
import json
import os
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from bot.llm import Parser  # noqa: E402

PROD_INV = [
    "- яйца: 8 шт", "- творог 5%: 1 уп", "- огурец: 1 шт", "- черри-помидоры: 210 г",
    "- курица: 250 г", "- сыр: 100 г", "- клубника: 1 уп", "- микс овощей: 1 уп",
    "- яблоки: 2 шт", "- бананы: 2 шт", "- йогурт греческий: 1 банка",
]
CLARIFY_INV = [line if "клубника" not in line else "- клубника: есть"
               for line in PROD_INV]
DINNER_INV = ["- греча: 3 пакетик", "- куриное филе: есть", "- сыр: есть",
              "- огурцы: 2 шт", "- перец: 1 шт"]

# (метка, автор, текст, холодильник) — ожидание в комментарии
CASES = [
    # meal + консервативные списания: упаковки без qty, штучное с qty
    ("A частичное употребление", "Никита",
     "завтрак съел 05.07.\n\nКаша овсянка, порезанная клубника, йогурт греческий, "
     "ложка мёда\n\nсытость 3,5/5.\nвкус 2/5.\n\nсначала вышло очень кисло, добавил "
     "мёда, но плохо перемешал, и стало местами сладко, местами кисло.", PROD_INV),
    # словесная поправка учёта возвращает кончившуюся позицию (set)
    ("B коррекция учёта", "Никита",
     "Нет, клубника не кончилась. То, что было ранее в холодильнике - актуально",
     [line if "клубника" not in line else "- клубника: кончилось" for line in PROD_INV]),
    # регрессия: штучное с числом, остальное без qty
    ("C ужин со штучным", "Никита",
     "поел, греча + сыр + курица + перец, огурец\n\nсытость 5/5\n\nвкус 4,5/5",
     DINNER_INV),
    # «не в холодильнике, а рядом» не отменяет учёт; числа прописью
    ("D добавление с уточнением места", "Никита",
     "У меня есть два яблока и 2 банана, не в холодильнике, а рядом.", DINNER_INV),
    # «было X г» вскрытой упаковки ≠ остаток; закрытая = полная; известный остаток не трётся
    ("E уточнение с размерами упаковок", "Никита",
     "Уточнение по холодильнику:\n\nКлубника вскрытая упаковка, было 300г, сейчас "
     "больше половины. Сыр вскрытая упаковка, было 400г. Помидоры черри, вскрытая "
     "упаковка, было 250г. Творог закрытый, 5%, 200 грам.\n\nИ то, что не добавил:\n\n"
     "Масло подсолнечное 0,9л. Вскрытая упаковка рис длиннозерный, было 900г.",
     CLARIFY_INV),
    # телеграфный обрывок без сигнала → chatter
    ("F обрывок Богдана", "Богдан", "Овощи ещё", PROD_INV),
    # короткое, но с глаголом → inventory
    ("G телеграфно, но с глаголом", "Никита", "купил хлеб", PROD_INV),
    # «сытость» без числа → satiety НЕ заполняется
    ("H оценка без числа", "Никита",
     "Обед 05.07.\n\nспагетти + курица + помидоры.\n\nсытость\nвкус 3/5, не хватает "
     "соуса или салата", PROD_INV),
    # поправка отчёта: is_correction=true (ops игнорируются кодом)
    ("I поправка отчёта", "Никита",
     "Ещё поправка.\n\nОбед 05.07.\n\nспагетти + курица + помидоры.\n\nсытость 3/5\n"
     "вкус 3/5\n\nКурица ещё осталась, на ужин.", PROD_INV),
    # синоним матчится с существующей (в т.ч. кончившейся) позицией
    ("J закупка-синоним", "Богдан",
     "Купить:\nФиле грудки цыплят-бройлеров 800г\nХлеб чёрный 1 шт",
     ["- курица: кончилось", "- яйца: 8 шт", "- сыр: есть"]),
    # разные продукты одной категории НЕ сливаются
    ("K категория ≠ продукт", "Никита",
     "купил помидоры обычные 3 шт и цыплёнка охлаждённого 500 г",
     ["- черри-помидоры: 250 г", "- курица: 800 г", "- макароны makfa рожки: 450 г"]),
    # сырьё vs готовое: «X (готово)» отдельной позицией
    ("L сырьё vs готовое", "Никита",
     "булгур осталось 2 пакетика + 1 готовый в холодильнике. курица половина "
     "осталась, порезанная кубиками.",
     ["- булгур в пакетиках: 5 шт", "- курица: 800 г"]),
    # съел готовое → списывается готовая позиция, не сырьё
    ("M съел готовое", "Никита",
     "поужинал готовым булгуром с курицей, сытость 4/5 вкус 4/5",
     ["- булгур в пакетиках: 2 шт", "- булгур (готово): 1 порция", "- курица: 400 г"]),
    # оператор «минус X» → subtract (а не обрывок)
    ("N оператор минус", "Никита", "минус огурец", ["- огурец: 2 шт", "- яйца: 8 шт"]),
]


async def main() -> None:
    parser = Parser(os.environ["ANTHROPIC_API_KEY"])
    for label, author, text, inv in CASES:
        result = await parser.parse(text, author, "2026-07-06 (понедельник) 14:00", inv)
        print(f"=== {label}")
        print(json.dumps(result, ensure_ascii=False, indent=1))


if __name__ == "__main__":
    asyncio.run(main())
